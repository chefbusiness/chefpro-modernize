/**
 * AI Chef Pro — Digital Products Config (single source of truth)
 *
 * This is the ONLY place to register a new digital product. Any change here
 * propagates automatically to:
 *   - netlify/functions/verify-purchase.ts      (post-purchase email + JWT)
 *   - netlify/functions/resend-access.ts        ("ya compré" magic link email)
 *   - netlify/functions/admin-generate-access.ts (manual magic link)
 *   - netlify/functions/get-download-urls.ts    (download paths per product)
 *   - src/pages/AdminGenerateAccess.tsx         (admin dropdown)
 *
 * Adding a new product = add one entry here. The admin dropdown auto-populates,
 * the magic-link emails work out of the box, and downloads resolve correctly.
 *
 * Special cases:
 *   - 'pro-prompts-ebook' has files: {} because its downloads are env-var-backed
 *     PDFs (PDF_EBOOK_URL, PDF_BONUS1_URL, PDF_BONUS23_URL). The special branch
 *     in get-download-urls.ts handles it.
 *   - 'mega-pack-tareas' files use a `kit-id__file-key` prefix scheme to
 *     namespace the bundled kits' downloads.
 */

export interface ProductDigitalConfig {
  /** Slug — must match the productId used in *AccessGate wrappers. */
  id: string;
  /** Short human name without price (e.g. "Kit de Escandallos Pro"). */
  name: string;
  /** Price label for admin UI (e.g. "€12", "€18,50"). */
  priceLabel: string;
  /** Front-end magic link landing path (no leading host). */
  accessPath: string;
  /** Email subject line (same for post-purchase and resend). */
  emailSubject: string;
  /** Post-purchase email title (e.g. "¡Gracias por tu compra!"). */
  emailTitle: string;
  /** Post-purchase email body HTML fragment. */
  emailBodyPostPurchase: string;
  /** Email CTA button label. */
  emailCta: string;
  /** Resend ("ya compré") email title. */
  emailTitleResend: string;
  /** Resend email body HTML fragment. */
  emailBodyResend: string;
  /** Map of download key → /dl/... path (relative to /public). Empty for env-var-backed products. */
  files: Record<string, string>;
}

