const STUB_LANGS: LangCode[] = ['en', 'fr', 'de', 'it', 'pt', 'nl'];

function makeStubContent(es: UseCaseContent): Record<LangCode, UseCaseContent> {
  const out: Record<LangCode, UseCaseContent> = { es } as Record<LangCode, UseCaseContent>;
  for (const lang of STUB_LANGS) {
    out[lang] = es;
  }
  return out;
}

